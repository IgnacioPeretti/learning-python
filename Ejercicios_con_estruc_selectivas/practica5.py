
"""""
Ingresar un número entero N, calcular y mostrar la sumatoria de los 5 enteros posteriores a N.
"""""

N = int(input("Ingresar un número entero N: "))


sumatoria = (N + 1) + (N + 2) + (N + 3) + (N + 4) + (N + 5)

print("La sumatoria de los 5 enteros posteriores a N es:", sumatoria)

